    private static LoeesenEngEesti convert(Node                row,
                                           Map<String, String> mp3Map,
                                           Map<String, String> svgMap) {
        Element  e         = ((Element)row);
        boolean  isVariant = StringUtils.contains(e.getAttribute("class"), "variation");
        String   dataId    = e.getAttribute("data-id");
        NodeList tds       = e.getElementsByTagName("td");
        String   en        = isVariant ? "ðŸŒµ" : tds.item(1).getTextContent();
        String   ee        = tds.item(2).getTextContent();
        String   img       = String.format("/OrizonFlash_V2/ressources/svg/%s", svgMap.get(dataId));
        String   mp3       = String.format("https://www.loecsen.com/OrizonFlash_V2/ressources/son/%s", mp3Map.get(dataId));
        return new LoeesenEngEesti(
            en, //StringEscapeUtils.unescapeHtml is not required because of xml.dom.getTextContent
            ee,
            mp3,
            img,
            dataId);
    }

    private static void swapVariant(List<LoeesenEngEesti> enees) {
        for (int i = 0; i < enees.size(); i++) {
            LoeesenEngEesti enee = enees.get(i);
            if (StringUtils.equals(enee.getEng(), "ðŸŒµ")) {
                LoeesenEngEesti rep
                    = new LoeesenEngEesti(
                        enees.get(i - 1).getEng(),
                        enee.getEesti(),
                        enee.getMp3(),
                        enee.getImg(),
                        enee.getDataId());
                enees.set(i, rep);
            }
        }
    }

    private static void processPage(NodeList rows) throws Exception {
        Map<String, String> mp3Map = buildMp3Map();
        Map<String, String> svgMap = buildSvgMap();

        List<LoeesenEngEesti> enees
            = IntStream
                  .range(0, rows.getLength())
                  .mapToObj(rows::item)
                  .map(r -> convert(r, mp3Map, svgMap))
                  .collect(Collectors.toList());
        swapVariant(enees);

        List<String> tsv = new ArrayList<>();
        enees.forEach(enee -> {
            String en = StringEscapeUtils.escapeCsv(enee.getEng());
            String ee = StringEscapeUtils.escapeCsv(enee.getEesti());

            File mp3;
            try {
                mp3 = OeesenEngEestiUtils.downloadMp3(enee);
            } catch (Exception ex) {
                throw new IllegalStateException("exception at mp3 download", ex);
            }

            File png;
            try {
                File   svg      = OeesenEngEestiUtils.downloadSvg(enee);
                String basename = FilenameUtils.removeExtension(svg.getName());
                File   target   = new File("loeesen/" + basename + ".png");
                png             = ImagemagickUtils.convert(svg, target);
            } catch (Exception ex) {
                throw new IllegalStateException("exception at img processing", ex);
            }

            String l = String.format("%s\t<img src=\"%s\">\t%s\t[sound:%s]", en, png.getName(), ee, mp3.getName());
            tsv.add(l.replace("Âž", "Å¾").replace("Âš", "Å¡")); //TÅ¡au! OranÅ¾
            System.out.println(en + "\n" + ee + "\n" + png.getName() + "\n" + mp3.getName());
        });
        FileUtils.writeLines(new File("loeesen/anki.txt"), "UTF-8", tsv, true);
    }

    public static void main(String args[]) throws Exception {
        new File("loeesen").mkdir();
        URL    u       = new URL("https://www.loecsen.com/en/vocabulary-estonian");
        String page    = IOUtils.toString(u, "UTF-8");
        File   topPage = new File("loeesen/vocabulary-estonian");
        FileUtils.writeStringToFile(topPage, page, "UTF-8");

        String   s   = FileUtils.readFileToString(topPage, "UTF-8");
        Document doc = DomUtils.parseHtml(s);

        XPathFactory    xPathFactory = XPathFactory.newInstance();
        XPath           xpath        = xPathFactory.newXPath();
        XPathExpression tableXp      = xpath.compile("//table[@class='printDiv']");

        NodeList tables = (NodeList)tableXp.evaluate(doc, XPathConstants.NODESET);
        for (int i = 0; i < tables.getLength(); i++) {
            Element  table = (Element)tables.item(i);
            Element  tbody = (Element)table.getElementsByTagName("tbody").item(0);
            NodeList rows  = tbody.getElementsByTagName("tr");
            processPage(rows);
        }
    }

